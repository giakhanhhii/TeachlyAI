function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function createCard(id, front, back, hint, phonetic = "") {
  const card = {
    id,
    front: String(front || "").trim(),
    back: String(back || "").trim(),
  };
  const safeHint = String(hint || "").trim();
  const safePhonetic = String(phonetic || "").trim();
  if (safeHint) card.hint = safeHint;
  if (safePhonetic) card.phonetic = safePhonetic;
  return card;
}

function buildDefaultCardIndexes(count) {
  const want = Math.max(1, Math.min(40, Number(count) || 20));
  if (want >= 40) return Array.from({ length: 40 }, (_, index) => index + 1);
  const picked = new Set([1, 2, 3, 40]);
  for (let i = 1; picked.size < want && i <= 38; i += 1) {
    const index = 3 + Math.round((i * 36) / Math.max(1, want - 3));
    picked.add(Math.max(4, Math.min(39, index)));
  }
  return Array.from(picked).sort((a, b) => a - b).slice(0, want);
}

function pickCardsByIndexes(cards, indexes) {
  const picked = indexes.map((index) => cards[index - 1]).filter(Boolean);
  return picked.length ? picked : cards.slice();
}

function buildPreset(raw) {
  const cards = raw.entries.map((entry, index) =>
    createCard(`${raw.id}-${String(index + 1).padStart(2, "0")}`, entry[0], entry[1], entry[2], entry[3] || ""),
  );
  const defaultCards = pickCardsByIndexes(cards, buildDefaultCardIndexes(raw.count));
  return {
    id: raw.id,
    topic: raw.topic,
    basis: raw.basis,
    count: String(raw.count),
    notes: raw.notes,
    cards,
    defaultCards,
  };
}

